#include <stdio.h>

int main()
{
    int l, k;
    scanf("%d %d", &l, &k);

    // 定义 dp 数组，dp[i][0] 表示高度为 i，顶部为浅色木块的方案数
    // dp[i][1] 表示高度为 i，顶部为深色木块的方案数
    int dp[l + 1][2];
    for (int i = 0; i <= l; i++)
    {
        dp[i][0] = dp[i][1] = 0;
    }

    // 初始化高度为 1 的初始状态
    dp[1][0] = 0; // 高度为 1，顶部为浅色木块，不合法
    dp[1][1] = 1; // 高度为 1，顶部为深色木块，合法

    // 遍历所有可能的高度，从 2 到 l
    for (int i = 2; i <= l; i++)
    {
        // 如果顶部为浅色木块，则上一个木块必须为深色木块
        dp[i][0] = dp[i - 1][1];
        // 如果顶部为深色木块，则上一个木块可以为浅色木块或深色厚木块
        dp[i][1] = dp[i - 1][0];
        if (i >= k)
        {
            dp[i][1] += dp[i - k][0];
        }
    }

    // 计算总的合法方案数
    int total = 0;
    for (int i = 1; i <= l; i++)
    {
        total += dp[i][1];
    }

    printf("%d\n", total);

    return 0;
}